import React from 'react';
import { Helmet } from 'react-helmet-async';

interface SEOHeadProps {
  title?: string;
  description?: string;
  keywords?: string;
  image?: string;
  url?: string;
  type?: 'website' | 'article' | 'product';
  structuredData?: object;
  noIndex?: boolean;
}

const SEOHead: React.FC<SEOHeadProps> = ({
  title = "KMASTAYS Dubai - Premium Corporate Serviced Apartments",
  description = "Premium corporate serviced apartments in Dubai's prime business districts. Luxury accommodation for business travelers in Downtown Dubai, Business Bay, DIFC & Dubai Marina.",
  keywords = "Dubai corporate apartments, serviced apartments Dubai, business accommodation Dubai, DIFC apartments, Business Bay apartments, Downtown Dubai housing",
  image = "https://kmastays.com/og-image.jpg",
  url = "https://kmastays.com",
  type = "website",
  structuredData,
  noIndex = false
}) => {
  const fullTitle = title.includes('KMASTAYS') ? title : `${title} | KMASTAYS Dubai`;
  
  return (
    <Helmet>
      {/* Primary Meta Tags */}
      <title>{fullTitle}</title>
      <meta name="title" content={fullTitle} />
      <meta name="description" content={description} />
      <meta name="keywords" content={keywords} />
      <meta name="robots" content={noIndex ? "noindex, nofollow" : "index, follow, max-image-preview:large, max-snippet:-1, max-video-preview:-1"} />
      
      {/* Open Graph / Facebook */}
      <meta property="og:type" content={type} />
      <meta property="og:url" content={url} />
      <meta property="og:title" content={fullTitle} />
      <meta property="og:description" content={description} />
      <meta property="og:image" content={image} />
      <meta property="og:image:width" content="1200" />
      <meta property="og:image:height" content="630" />
      <meta property="og:site_name" content="KMASTAYS Dubai" />
      <meta property="og:locale" content="en_AE" />
      
      {/* Twitter */}
      <meta property="twitter:card" content="summary_large_image" />
      <meta property="twitter:url" content={url} />
      <meta property="twitter:title" content={fullTitle} />
      <meta property="twitter:description" content={description} />
      <meta property="twitter:image" content={image} />
      
      {/* Canonical URL */}
      <link rel="canonical" href={url} />
      
      {/* Structured Data */}
      {structuredData && (
        <script type="application/ld+json">
          {JSON.stringify(structuredData)}
        </script>
      )}
    </Helmet>
  );
};

export default SEOHead;
